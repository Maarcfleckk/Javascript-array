/**
 * Given 'MONKEYS' array, define function 'feedMonkeys' to return an array where each monkey has one fruit
 * use for() instruction
 *
 * expected: with 🍌 => ['🐒 🍌', '🦍 🍌', '🦧 🍌']
 * expected: with 🍎 => ['🐒 🍎', '🦍 🍎', '🦧 🍎']
 */

const MONKEYS = ["🐒", "🦍", "🦧"];

export const feedMonkeys = function(fruit){
    const mappedMonkeys = MONKEYS.map((monkey) => {
        const currentMonkey = `${monkey} ${fruit}`;
        return currentMonkey;
    })
    return mappedMonkeys;
};
